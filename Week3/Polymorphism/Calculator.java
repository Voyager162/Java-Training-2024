package Week3.Polymorphism;

public class Calculator {
    public int division(int one, int two) {
        int finalInt;
        finalInt = one / two;
        return finalInt;
    }

    public double division(double one, double two) {
        double finalInt;
        finalInt = one / two;
        return finalInt;
    }

    public int multiplication(int one, int  two) {
        int finalInt;
        finalInt = one * two;
        return finalInt;
    }
}